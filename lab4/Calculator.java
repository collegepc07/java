package lab4;

public class Calculator {
	float Addition;
	float Subtraction;
	float Multiply;
	float Divide;
	float Modulus;
	
	public float getAddition(){
		return Addition;
	}
	public float setAddition(float Addition) {
		return this.Addition = Addition;
	}
	public float getSubtraction(){
		return Subtraction;
	}
	public float setSubtraction(float Subtraction) {
		return this.Subtraction = Subtraction;
	}
	public float getMultiply(){
		return Multiply;
	}
	public float setMultiply(float Multiply) {
		return this.Multiply = Multiply;
	}
	public float getDivide(){
		return Divide;
	}
	public float setDivide(float Divide) {
		return this.Divide = Divide;
	}
	public float getModulus(){
		return Modulus;
	}
	public float setModulus(float Modulus) {
		return this.Modulus = Modulus;
	}
	
	
}
